use std::sync::Arc;

#[derive(Debug, Clone, PartialEq)]
pub enum Token {
	Data,
	Define,
	Function,
	Module,
	Export,
	Use,
	SelfVariable,
	Identifier(Arc<str>),
	Integer(i128),
	Truth(bool),
	ParenthesisOpen,
	ParenthesisClose,
	BlockOpen,
	BlockClose,
	Dot,
	Separator,
	ListSeparator,
	ReturnSeparator,
	PathSeparator,
	Wildcard,
	Asterisk,
	Reference,
	Compile,
	Let,
	Loop,
	Drop,
	If,
	Assign,
	AngleLeft,
	AngleRight,
	LessEqual,
	GreaterEqual,
	Equality,
	Implies,
	Swap,
	Template,
	Unique,
	Prime,
	Add,
	Minus,
	Multiply,
	AddAssign,
	MinusAssign,
	MultiplyAssign,
	LineBreak,
	End,
}

#[derive(Debug, PartialEq, Clone)]
pub enum LexerToken {
	Token(Token),
	Indent,
}
