#[derive(Debug, Clone, PartialEq)]
pub enum Token<'a> {
	Comment(&'a str),
	Function,
	Binding,
	Drop,
	While,
	ReturnSeparator,
	Identifier(&'a str),
	ParenthesisOpen,
	ParenthesisClose,
	BlockOpen,
	BlockClose,
	VariableSeparator,
	ListSeparator,
	Terminator,
	MutableModifier,
	Assign,
	Equal,
	LessThan,
	LessThanEqual,
	Swap,
	Implies,
	Add,
	Minus,
	Multiply,
	AddAssign,
	UnsignedInteger(u64),
}
